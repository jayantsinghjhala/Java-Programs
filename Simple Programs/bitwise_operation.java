class bitwise {
     public static void main(String args[])
     {
          int x=2,y=3;
          System.out.println("Bitwise AND : "+x+" & "+y+" = "+(x&y));
          System.out.println("Bitwise OR : "+x+" | "+y+" = "+(x|y));
          System.out.println("Bitwise XOR : "+x+" ^ "+y+" = "+(x^y));
          System.out.println("Bitwise NOT : ~"+x+" = "+(~x));
     }
     
}
